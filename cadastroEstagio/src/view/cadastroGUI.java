    private void jBGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGravarActionPerformed
        Cadastro pessoa;
        if (jTFNome.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Informe o Nome", "Aviso", 0);
            jTFNome.requestFocusInWindow();
            return;

        } else if (jTFEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o E-mail", "Aviso", 0);
            jTFEmail.requestFocusInWindow();
            return;
        
        } else if (jTFSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe a Senha", "Aviso", 0);
            jTFSenha.requestFocusInWindow();
            return;
        }
    

        if (cadastrados.validaEmail(jTFEmail.getText())) {

            //String nome, int idade, String cpf, String bairro
            pessoa = new Cadastro(jTFNome.getText(), jTFEmail.getText(), jTFSenha.getText());

            cadastrados.addCadastro(pessoa);
            
            this.setVisible(false);}
         else {
            JOptionPane.showMessageDialog(this, "Email j√° cadastrado!", "Aviso", 0);
        }
                
        
        
        
    
    }//GEN-LAST:event_jBGravarActionPerformed

